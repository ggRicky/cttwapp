    }

    /**
     * @inheritdoc
     */
    public function rules()
    {
        return [
            [['name', 'type'], 'required'],
            [['type', 'created_at', 'updated_at', 'rule_name', 'data'], 'default', 'value' => null],
            [['type', 'created_at', 'updated_at'], 'integer'],

            // 2018-05-26 : In the next rule, the 'data' field is moved into the above list with default null value.

            [['description'], 'string'],
            [['name', 'rule_name'], 'string', 'max' => 64],
            [['name'], 'unique'],

